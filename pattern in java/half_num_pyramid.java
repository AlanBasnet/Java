        // half pyramid with number
/*
1
12
123
1234
 */

public class half_num_pyramid {
        public static void main(String[]args){
            int m=4 ;
            for(int i=1 ; i<=m;i++){
                for(int j=1;j<=i;j++){
                    System.out.print(j);
                }
                System.out.println();
            }
        }

    
}
